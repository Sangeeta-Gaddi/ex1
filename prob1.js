//Declare different types of variable and print type of each//

//We can declare variable using let, var  and const keyword//

//using let keyword//
var num=45;
console.log(typeof(num));

let str="Nishita";
console.log(typeof(str));

const bool=false;
console.log(typeof(bool));

 var array=[1,2,3,4];
 console.log(typeof array);

 let obj={name:"Nishita", dob:17/5/2001};
 console.log(typeof(obj));

 const func=function(){};
 console.log(typeof(func));

 //output//
//  number
// string
// boolean
// object
// object
// function

